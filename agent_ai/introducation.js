// ai agents kya hota hai ?
// AI agents aise software programs hote hain jo apne environment ke sath interact karte hain aur
// apne goals ko achieve karne ke liye decision lete hain. Ye agents machine learning, natural
//  language processing, aur other AI techniques ka use karke apne tasks
//  ko perform karte hain. AI agents ka use customer service, virtual assistants,
// recommendation systems, aur autonomous vehicles jaise areas mein hota hai.

// AI agents ke types kya hote hain ?
// 1. Reactive Agents: Ye agents apne current environment ke basis par decisions
// lete hain aur past experiences ko consider nahi karte. Ye simple tasks ke liye
// suitable hote hain.

// 2. Deliberative Agents: Ye agents apne goals ko achieve karne ke liye
// planning aur reasoning ka use karte hain. Ye complex tasks ke liye
// suitable hote hain.

// 3. Hybrid Agents: Ye agents reactive aur deliberative approaches ka
// combination hote hain, jisse wo flexible aur adaptable hote hain.

// AI agents ke applications kya hote hain ?
// 1. Customer Service: AI agents customer queries ko handle karne ke liye
// use hote hain, jisse customer satisfaction improve hoti hai.
// 2. Virtual Assistants: AI agents virtual assistants jaise Siri, Alexa, aur Google Assistant mein use hote hain, jo users ke questions ka jawab dete hain aur tasks perform karte hain.
// 3. Recommendation Systems: AI agents recommendation systems mein use hote hain, jisse users ko personalized recommendations milti hain.
// 4. Autonomous Vehicles: AI agents autonomous vehicles mein use hote hain, jisse vehicles
// apne environment ke sath interact karte hain aur safe driving decisions lete hain.

// AI agents ke benefits kya hote hain ?
// 1. Efficiency: AI agents repetitive tasks ko efficiently perform karte hain, jisse human effort save hota hai.
// 2. Accuracy: AI agents data analysis aur decision making mein high accuracy provide karte hain.
// 3. Scalability: AI agents large scale operations ko handle karne mein capable hote hain.
// 4. Personalization: AI agents personalized experiences provide karte hain, jisse user satisfaction improve hoti hai.

